package edu.cornell.pserc.jpower.options;

/**
 * Amount of progress info printed.
 */
public enum Verbosity {

	/**
	 * Print no progress info.
	 */
	ZERO,

	/**
	 * Print a little progress info.
	 */
	LITTLE,

	/**
	 * Print a lot of progress info.
	 */
	LOTS,

	/**
	 * Print all progress info.
	 */
	ALL;

}
